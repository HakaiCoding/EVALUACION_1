//LOOPS

//for loop
for (let i = 0; i < 10; i++) {
    console.log(i);
}

//while loop
let x = 0;
while (x < 10) {
    console.log(x);
    x++;
}

//do while loop
let a = 0;
do {
    console.log(a);
    a++;
} while (a < 10);

//for in loop ONLY OBJECTS
let array = [1,2,3,4,5];
for (let z in array) {
    console.log(z);
}
